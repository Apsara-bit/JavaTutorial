
public class singleclass5 {
	singleclass5(int a, int b){
		System.out.println(a+b);
		
	}
	singleclass5(int c,int d,int e){
		System.out.println(c+d+e);
	}

	public static void main(String args[]) {
		singleclass5 obj=new singleclass5(10,20);
		singleclass5 obj1=new singleclass5(5,10,15);
	}
}





